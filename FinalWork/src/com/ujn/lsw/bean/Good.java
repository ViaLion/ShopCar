package com.ujn.lsw.bean;

public class Good {
	String nameString;
	int priceString;
	int numberString;
	String colorString;
	String versonString;
	
	public String getNameString() {
		return nameString;
	}
	public void setNameString(String nameString) {
		this.nameString = nameString;
	}
	public int getPriceString() {
		return priceString;
	}
	public void setPriceString(int priceString) {
		this.priceString = priceString;
	}
	public int getNumberString() {
		return numberString;
	}
	public void setNumberString(int numberString) {
		this.numberString = numberString;
	}
	public String getColorString() {
		return colorString;
	}
	public void setColorString(String colorString) {
		this.colorString = colorString;
	}
	public String getVersonString() {
		return versonString;
	}
	public void setVersonString(String versonString) {
		this.versonString = versonString;
	}
	
	
	
}
